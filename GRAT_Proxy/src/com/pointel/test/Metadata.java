package com.pointel.test;

public class Metadata {
	 private String elapsedTime;
	 private String elasticTime;
	 private String offset;
	 private String psize;
	 private String total;
	 private String wildCardResp;


	 // Getter Methods 

	 public String getElapsedTime() {
	  return elapsedTime;
	 }

	 public String getElasticTime() {
	  return elasticTime;
	 }

	 public String getOffset() {
	  return offset;
	 }

	 public String getPsize() {
	  return psize;
	 }

	 public String getTotal() {
	  return total;
	 }

	 public String getWildCardResp() {
	  return wildCardResp;
	 }

	 // Setter Methods 

	 public void setElapsedTime(String elapsedTime) {
	  this.elapsedTime = elapsedTime;
	 }

	 public void setElasticTime(String elasticTime) {
	  this.elasticTime = elasticTime;
	 }

	 public void setOffset(String offset) {
	  this.offset = offset;
	 }

	 public void setPsize(String psize) {
	  this.psize = psize;
	 }

	 public void setTotal(String total) {
	  this.total = total;
	 }

	 public void setWildCardResp(String wildCardResp) {
	  this.wildCardResp = wildCardResp;
	 }
	}